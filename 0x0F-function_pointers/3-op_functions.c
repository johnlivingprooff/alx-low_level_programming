#include "3-calc.h"

/**
 * op_add - addition function
 * @a: number to be added
 * @b: number to be added
 * Return: sum of a & b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts function
 * @a: number to be used
 * @b: number to be used
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies function
 * @a: number to be used
 * @b: number to be used
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides function
 * @a: number to be used
 * @b: number to be used
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulus function
 * @a: number to be used
 * @b: number to be used
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
