#include <stdio.h>
#include <math.h>

/**
 * add - adds two numbers
 * @a: number
 * @b: number
 * Return: Sum of a & b
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts two numbers
 * @a: number
 * @b: number
 * Return: subtracts b from a
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies two numbers
 * @a: number
 * @b: integer
 * Return: a * b
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides two numbers
 * @a: number
 * @b: number
 * Return: division of a by b
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - modulus
 * @a: number
 * @b: divisor
 * Return: remainder
 */
int mod(int a, int b)
{
	return (a % b);
}
